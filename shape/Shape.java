package shape;

public class Shape {
	private double area;
	private double perimeter;
	
	public double getArea(){
		return area;
	}
	
	public double getPerimeter(){
		return perimeter;
	}
	
	public void setArea(double x){
		area = x;
	}
	
	public void setPerimeter(double x){
		perimeter = x;
	}
}
